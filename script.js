let body = document.getElementsByTagName("body")[0]

function lightMode() {

    onclick = body.style.backgroundColor = "#fff";
    let icon = document.getElementsByClassName("icon")[0];
    icon.style.backgroundColor = "#6060ff";
}

function darkMode() {

    onclick = body.style.backgroundColor = "#101010";
    let icon = document.getElementsByClassName("icon")[0];
    icon.style.backgroundColor = "#212121";

}

